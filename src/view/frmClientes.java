    private void mnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnAlterarActionPerformed
       // Se edicao for false entao cadastrar
        if (!edicaoAtiva) {
            edicaoAtiva = true;
            btnAlterar.setText("Salvar altera√ß√£o");
            mnAlterar.setText("Salvar altera√ß√£o");
            modoEdicao(true);
            return;
        }

        // A partir deste excuta a Edi√ß√£o
        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher NOME do cliente");
        } else if (txtCEP.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher CEP do cliente");
        } else if (txtRua.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher RUA do cliente");
        } else if (txtBairro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher BAIRRO do cliente");
        } else if (txtNumero.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher NUMERO do cliente");
        } else if (txtCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher CIDADE do cliente");
        } else if (txtEstado.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher ESTADO do cliente");
        } else if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher EMAIL do cliente");
        } else if (txtCelular.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher CELULAR do cliente");
        } else {
            // Altera o Cliente
            int resp = JOptionPane.showConfirmDialog(this, "Deseja confirmar a altea√ß√£o?");
            if (resp == JOptionPane.YES_OPTION) {
                String cli = new ClienteDAO().alteraClinte(gravaCliente());
                if (cli == "OK") {
                    JOptionPane.showMessageDialog(null, "Cliente do CPF " + txtCPF.getText() + " Alterado!!!");
                } else {
                    JOptionPane.showMessageDialog(null, cli);
                }
            }

            // Reset do programa
            resetCampos();

            // Reset do botao alterar
            btnCadastrar.setEnabled(true);
            mnCadastrar.setEnabled(true);
            
            btnAlterar.setEnabled(false);
            mnAlterar.setEnabled(false);
            
            btnCancelar.setEnabled(false);
            mnCancelar.setEnabled(false);
            
            txtCPF.setText("");
            btnAlterar.setText("Alterar");
            mnAlterar.setText("Alterar");
            edicaoAtiva = false;
            
            btnAlugar.setEnabled(false); // Desabilita aluguel
            mnAlugar.setEnabled(false); // Desabilita aluguel
        }
    }//GEN-LAST:event_mnAlterarActionPerformed

