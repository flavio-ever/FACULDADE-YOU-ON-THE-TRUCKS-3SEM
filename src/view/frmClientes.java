    private String[] dataAluguel(int qtdDias) {
        Date dt = new Date();
        Calendar c = Calendar.getInstance();
        c.setTime(dt);
        c.add(Calendar.DATE, qtdDias);
        dt = c.getTime();

        SimpleDateFormat formatador = new SimpleDateFormat("yyyy-MM-dd");

        String[] xs = new String[]{formatador.format(dt), formatador.format(new Date())}; //added []

        return xs;
    }

    private void montaAlugados() {
        Float precoTotal = gVeiculo.getPrecoDia() * (cbQtd.getSelectedIndex() + 1);
        String[] data = dataAluguel(cbQtd.getSelectedIndex() + 1);

        lbTotal.setText("R$" + precoTotal);

        // Seta possiveis datas
        gAlugados.setDataAluguel(data[1]);
        gAlugados.setDataRetorno(data[0]);
        // Outros
        gAlugados.setPrecoTotal(precoTotal);
        gAlugados.setPlaca(gVeiculo.getPlaca());
        gAlugados.setCpf(gCliente.getCpf());
    }

