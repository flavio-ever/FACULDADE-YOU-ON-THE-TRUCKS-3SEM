    private void txtCPFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCPFKeyReleased
        Util map = new Util();

        if (map.cpfIsValid(txtCPF.getText())) {
            Cliente cli = new ClienteDAO().buscaCliente(
                    map.parserCpf(txtCPF.getText())
            );

            try{
            if (cli != null) {
                txtCPF.setText(cli.getCpf());
                txtNome.setText(cli.getNome());
                cbSexo.setSelectedItem(cli.getSexo());
                txtCEP.setText(cli.getCep());
                txtRua.setText(cli.getRua());
                txtBairro.setText(cli.getBairro());
                txtNumero.setText(cli.getNumero());
                txtComplemento.setText(cli.getComplemento());
                txtCidade.setText(cli.getCidade());
                txtEstado.setText(cli.getEstado());
                txtEmail.setText(cli.getEmail());
                txtCelular.setText(cli.getCelular());

                modoEdicao(false); // Bloqueia campos

                btnAlterar.setEnabled(true); // Libera botao
                mnAlterar.setEnabled(true); // Libera botao

                btnCadastrar.setEnabled(false); // Desabilita o cadastro
                mnCadastrar.setEnabled(false); // Desabilita o cadastro

                btnCancelar.setEnabled(true); // Habilita cancelar
                mnCancelar.setEnabled(true); // Habilita cancelar

                btnAlugar.setEnabled(true); // Habilita aluguel
                mnAlugar.setEnabled(true); // Habilita aluguel

                // Globaliza Cliente
                gCliente = cli;

                montaAlugados();

            } else {
                modoEdicao(true); // Libera campos

                resetCampos(); // Reseta os campos (exceto cpf)

                btnAlterar.setEnabled(false); // Desabilita botao
                mnAlterar.setEnabled(false); // Desabilita botao

                btnCadastrar.setEnabled(true); // Libera o cadastro
                mnCadastrar.setEnabled(true); // Libera o cadastro

                btnCancelar.setEnabled(false); // Desabilita cancelar
                mnCancelar.setEnabled(false); // Desabilita cancelar

                btnAlugar.setEnabled(false); // Habilita aluguel
                mnAlugar.setEnabled(false); // Habilita aluguel

                gCliente = null;
            }
            }
            catch(Exception e){
                e.getMessage();
            }

        }
    }//GEN-LAST:event_txtCPFKeyReleased

